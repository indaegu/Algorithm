import java.util.Scanner;
import java.io.FileInputStream;

/*
   사용하는 클래스명이 Solution 이어야 하므로, 가급적 Solution.java 를 사용할 것을 권장합니다.
   이러한 상황에서도 동일하게 java Solution 명령으로 프로그램을 수행해볼 수 있습니다.
 */
public class Solution
{
  public static void main(String args[]) throws Exception
  {
      Scanner sc = new Scanner(System.in);
      int k = sc.nextInt(); //int 형 입력 받음 ex)123
      String num = Integer.toString(k); // 123을 String형으로 변환
      int sum = 0; // 합을 담을 int 선언
      for(int i =1; i<=num.length();i++){ // string의 문자열 길이만큼 반복 ex) 3번반복
        sum += Character.getNumericValue(num.charAt(num.length()-i));
        // String에서 맨뒤문자부터 char형으로 꺼내옴 -> 해당 문자를 숫자로 바꾸고 sum에 더함
      }
    System.out.println(sum);
  }
}